// Program 1.7 Use a trigraph sequence
// You need to use -trigraphs to enable them
#include <stdio.h>

int main(void)
{
    printf("Use \?\?= to use ??= character\n");
    printf("Use \?\?' to use ??' character\n");
    printf("Use \?\?( to use ??( character\n");
    printf("Use \?\?< to use ??< character\n");
    printf("Use \?\?! to use ??! character\n");
    printf("Use \?\?> to use ??> character\n");
    printf("Use \?\?) to use ??) character\n");
    printf("Use \?\?- to use ??- character");
    return 0;
}